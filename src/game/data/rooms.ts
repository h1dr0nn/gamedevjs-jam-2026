/**
 * Room tile grid — 40 cols × 14 rows, each cell 32 px → 1280 × 448 px.
 *
 * Chars:
 *  '.' = empty (air)
 *  '#' = solid floor/wall
 *  '=' = one-way platform (solid top only)
 *  'H' = ladder
 *  'P' = player spawn
 *  'X' = exit door
 *  'S' = saw hazard
 *  'G' = gas vent hazard
 *  'E' = generator hazard
 *  'O' = pit (kills)
 *  '1' = drone spawn · '2' = gunner spawn · '3' = captain spawn
 */

export const ROOM_WIDTH = 40;
export const ROOM_HEIGHT = 14;

// Helper — ensure every row is exactly 40 chars (pads with '.')
function row40(s: string): string {
  return s.length >= 40 ? s.slice(0, 40) : s + '.'.repeat(40 - s.length);
}

function room(rows: string[]): string[] {
  return rows.map(row40);
}

