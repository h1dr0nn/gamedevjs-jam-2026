function parseRoom(id: string, grid: string[], kind: RoomTemplate['kind']): RoomTemplate {
  const spawns: RoomTemplate['spawns'] = [];
  const hazards: RoomTemplate['hazards'] = [];
  let chipSpawn: RoomTemplate['chipSpawn'];

  grid.forEach((gridRow, r) => {
    for (let c = 0; c < gridRow.length; c++) {
      const ch = gridRow[c];
      const x = c * 32 + 16;
      const y = r * 32 + 16;
      if (ch === '1') spawns.push({ type: 'drone' as EnemyId, x, y });
      else if (ch === '2') spawns.push({ type: 'gunner' as EnemyId, x, y });
      else if (ch === '3') spawns.push({ type: 'captain' as EnemyId, x, y });
      else if (ch === 'S') hazards.push({ type: 'saw' as HazardId, x, y });
      else if (ch === 'G') hazards.push({ type: 'gas' as HazardId, x, y });
      else if (ch === 'E') hazards.push({ type: 'generator' as HazardId, x, y });
      else if (ch === 'O') hazards.push({ type: 'pit' as HazardId, x, y });
      else if (ch === 'c') chipSpawn = { x, y };
    }
  });

  return { id, tiles: grid, spawns, hazards, chipSpawn, kind };
}

export const FLOOR_ROOMS: Record<number, RoomTemplate[]> = {
  1: FLOOR_1_ROOMS.map((g, i) => parseRoom(`1-${i + 1}`, g, i === 4 ? 'elite' : 'combat')),
  2: FLOOR_2_ROOMS.map((g, i) => parseRoom(`2-${i + 1}`, g, i === 4 ? 'elite' : 'combat')),
  3: FLOOR_3_ROOMS.map((g, i) => parseRoom(`3-${i + 1}`, g, i === 4 ? 'elite' : 'combat')),
};

export function getRoom(floorId: number, roomIndex: number): RoomTemplate {
  const rooms = FLOOR_ROOMS[floorId] ?? FLOOR_ROOMS[1];
  return rooms[roomIndex % rooms.length];
}
